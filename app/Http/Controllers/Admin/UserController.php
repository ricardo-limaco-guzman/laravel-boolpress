     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $role = Auth::user()->role;
        $redirect = $role === "admin" ? "admin.users.index" : "admin.home";
        $data = $request->all();
    
        $user->update($data);
        $user->userInfo()->updateOrCreate(["user_id" => $user->id], $data["userInfo"]);
    
        return redirect()->route($redirect)->with(["status" => "Dati aggiornati correttamente!"]);
    }

